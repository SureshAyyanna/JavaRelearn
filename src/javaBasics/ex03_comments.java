package javaBasics;

public class ex03_comments {
	public static void main(String[] args) {
		// Single line comment It will be ignored by JVM/ Compiler
		System.out.println("Single line comment");
		
		
		/*
		 * Multi line comment, It will be ignored by JVM/ Compiler
		 */
		System.out.println("Multi line comment");
	}
}
