package javaBasics02;

public class ex13_relationalOperator {
public static void main(String[] args) {
	// < Less Than
	// < = -> Less than or equal to
	// > Greater
	// > = Greater or equal
	// == -> Equal to (but checking)
	// != -> Not equal

	// All of them will result boolean output.

		int a = 10;
		int b = 30;
		boolean c = a > b; // 10> 30
		System.out.println(c);

		int age_suresh = 30;
		int age_Ramesh = 34;
		boolean result = age_suresh >= age_Ramesh;
		System.out.println(result);

	}
}
