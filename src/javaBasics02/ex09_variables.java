package javaBasics02;

public class ex09_variables {
	public static void main(String[] args) {
		// data type variable_name = variable_value
		int a = 10;

		int age = 30;
		 // int -> primitive - data type - numeric
	     // it store the whole number - without any decimal point
	     // age -> variable name or identifier
		
		age = 27;
		System.out.println(age); // 27

		age = 27 + 1;
		System.out.println(age);

		System.out.println(age++);
		System.out.println(++age);
		System.out.println(age--);
		System.out.println(--age);

	}
}
